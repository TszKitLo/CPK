import Foundation
import UIKit

extension UIColor {
    static func themeBlue() -> UIColor{
        return UIColor(red: 0/255, green: 188/255, blue: 212/255, alpha: 1)
    }
    
    static func themeOrange() -> UIColor {
        return UIColor(red: 255/255, green: 171/255, blue: 0/255, alpha: 1)    //green: 154
    }
    
    static func amberColor() -> UIColor {
        return UIColor(red: 255/255, green: 193/255, blue: 7/255, alpha: 1)
    }
    
    static func tealColor() -> UIColor {
        return UIColor(red: 0/255, green: 150/255, blue: 136/255, alpha: 1)
    }
    
    
}

